package com.diplomski.backend.utility;

import java.time.*;
import java.util.Date;

public class MyDateFormatterMapper {
     public static LocalDate convertToLocalDateViaInstant(Date dateToConvert) {
        return dateToConvert.toInstant()
                .atZone(ZoneId.systemDefault())
                .toLocalDate();
    }
     public static LocalDate convertToLocalDateViaMilisecond(Date dateToConvert) {
        return Instant.ofEpochMilli(dateToConvert.getTime())
                .atZone(ZoneId.systemDefault())
                .toLocalDate();
    }
    public static LocalDateTime convertToLocalDateTimeViaInstant(Date dateToConvert) {
        return dateToConvert.toInstant()
                .atZone(ZoneId.systemDefault())
                .toLocalDateTime();
    }

    public static LocalDateTime convertToLocalDateTimeViaMilisecond(Date dateToConvert) {
        return Instant.ofEpochMilli(dateToConvert.getTime())
                .atZone(ZoneId.systemDefault())
                .toLocalDateTime();
    }
    public static LocalDateTime createFromDateAndTime(Date date,LocalDateTime localDateTime){
        LocalTime time=localDateTime.toLocalTime();
        LocalDate newDate=convertToLocalDateViaInstant(date);
        return LocalDateTime.of(newDate,time);
    }

}
